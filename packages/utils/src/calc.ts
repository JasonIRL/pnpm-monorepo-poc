/**
 * Adds two numbers
 * @param {number} a
 * @param {numer} b
 * @returns {number}
 */
const add = (a: number, b: number): number => a + b;

/**
 * Subtracts two numbers
 * @param {number} a
 * @param {number} b
 * @returns {number}
 */
const subtract = (a: number, b: number): number => a - b;

export { add, subtract };
